/**
 * Return the cumulative list of controls applicable at a given impact level.
 * High includes Moderate which includes Low.
 */
export function getBaselineControls(family: string, impactLevel: string): string[] {
  const baseline = NIST_BASELINE[family];
  if (!baseline) return [];
  const level = (impactLevel ?? 'Low').toLowerCase();
  const controls = [...baseline.low];
  if (level === 'moderate' || level === 'high') controls.push(...baseline.moderate);
  if (level === 'high') controls.push(...baseline.high);
  return controls;
}
